
import java.*;

public class Account {

   private String name;
   private String age;
   private String gender;
   private String email;
   private String password;
    public Account(String name,String age, String gender, String email, String password){

        this.name = name;
        this.age = age;
        this.gender = gender;
        this.email = email;
        this.password = password;
    }


}
